#include <unistd.h>

int	main(int a, char *b[])
{
	int	i;
	int	j;
	int	ascii[256] = {0};

	if (a == 3)
	{
		i = 1;
		while (i < 3)
		{
			j = 0;
			while(b[i][j])
			{
				if(ascii[(int)b[i][j]] == 0)
				{
					ascii[(int)b[i][j]] = 1;
					write(1, &b[i][j], 1);
				}
				j++;
			}
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
