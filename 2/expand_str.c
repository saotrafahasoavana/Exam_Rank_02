#include <unistd.h>

int	main(int ac, char *a[])
{
	int	i;
	
	if (ac == 2)
	{
		i = 0;
		while (a[1][i] <= 32)
			i++;
		while (a[1][i])
		{
			if (a[1][i] <= 32)
			{
				if (a[1][i + 1] > 32)
					write(1, "   ", 3);
			}
			else
				write(1, &a[1][i], 1);
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
